private func viewRenderers(_ values: ViewRenderer.Type...) -> [ViewType: any ViewRenderer.Type] {
  return values.reduce(into: [ViewType: any ViewRenderer.Type]()) {
    $0[$1.type] = $1
  }
}

private func modifierRenderers(_ values: ModifierRenderer.Type...) -> [AttributeKey: any ModifierRenderer.Type] {
  return values.reduce(into: [AttributeKey: any ModifierRenderer.Type]()) {
    $0[$1.key] = $1
  }
}
